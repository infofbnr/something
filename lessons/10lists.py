numbers = [1,2,3,4,5] # this is a list
numbers[0] # returns 1
numbers[1] # returns 2
numbers[-1] # returns 5
numbers.append(6) # adds 6 to the end of the list
numbers.insert(0,6) # adds 6 to the index of 0, in this case 1
numbers.remove(6) # removes 6 from the list
numbers.pop() # removes the last item from the list
numbers.clear() # removes all the items from the list
numbers.index(8) # returns the index of the first 8 in the list
numbers.sort() # sorts the list
numbers.reverse() # reverses the list
numbers.copy() # returns a copy of the list
