# While loops and for loops in python:
# while loops:
i = 1
while i < 5:
    print(i)
    i+=1
# what this does is it prints the numbers 1 to 4 since they are the only numbers less than 5

# For loops
for i in range(1,5):
    print(i)
# what is range?
# range(5): generates 0,1,2,3,4
# range(1,5): generates 1,2,3,4
# range(1,5,2): generates 1,3 // range is (start, stop, step) so basically (10,50,5) means 10, 15, 20, 25, 30, 35, 40, 45 / its going up 5 by 5 starting from 10 till 50
